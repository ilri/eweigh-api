        <h3>About ICAO</h3>
        <p>
            The International Civil Aviation Organization (ICAO) is a UN specialized agency, 
            established by States in 1944 to manage the administration and governance of the 
            Convention on International Civil Aviation (Chicago Convention).
        </p>
        <p>
            ICAO works with the Convention’s 192 Member States and industry groups to reach 
            consensus on international civil aviation Standards and Recommended Practices 
            (SARPs) and policies in support of a safe, efficient, secure, economically 
            sustainable and environmentally responsible civil aviation sector. These SARPs 
            and policies are used by ICAO Member States to ensure that their local civil aviation 
            operations and regulations conform to global norms, which in turn permits more than 100,000 
            daily flights in aviation’s global network to operate safely and reliably in every region of the world.
        </p>
        <p>
            In addition to its core work resolving consensus-driven international SARPs and 
            policies among its Member States and industry, and among many other priorities and 
            programmes, ICAO also coordinates assistance and capacity building for States in 
            support of numerous aviation development objectives; produces global plans to coordinate 
            multilateral strategic progress for safety and air navigation; monitors and reports on 
            numerous air transport sector performance metrics; and audits States’ civil aviation 
            oversight capabilities in the areas of safety and security.
        </p>
        <p>
            For more information - <a href="https://www.icao.int/about-icao/Pages/default.aspx" target="_blank" rel="noopener noreferrer">https://www.icao.int/about-icao/Pages/default.aspx</a>
        </p>

        <h3>About KCAA</h3>
        <p>
            Kenya Civil Aviation Authority (KCAA) was established on 24th October 2002 by the Civil 
            Aviation (Amendment) Act, 2002 with the primary functions towards; Regulation and oversight 
            of Aviation Safety & Security; Economic regulation of Air Services and development of Civil 
            Aviation; Provision of Air Navigation Services, and Training of Aviation personnel KCAA; as 
            guided by the provisions of the convention on international civil aviation, related ICAO Standards 
            and Recommended Practices (SARPs), the Kenya Civil Aviation Act, 2013 and the civil aviation regulations.
        </p>

