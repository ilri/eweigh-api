        <h3>What is ICAN?</h3>
        <p>
            The eleventh ICAO Air Services Negotiation Event (ICAN2018) will be held in Nairobi, 
            Kenya from 10 to 14 December 2018. It will be hosted by the Kenya Civil Aviation Authority (KCAA).  
        </p>
        <p>
            ICAN2018 provides States with a central meeting place to conduct multiple bilateral (regional or 
            plurilateral) air services negotiations or consultations.
        </p>
        <p>
            It offers a forum for the participants to learn about current trends, discuss and exchange experiences, 
            as well as providing excellent networking opportunities. Negotiations between States/Regional Groups 
            stay private.
        </p>
        <p>
            ICAN has been highly commended by participating Delegations as an efficient platform for conducting air 
            service negotiations. Since its inception, the number of countries that utilizes this facility has steadily 
            increased. So far more than a total of 142 States have participated in and benefited from ICAN at least once.
        </p>
        <p>
            ICAN2018 will offer industry stakeholders opportunities for Business to Business (B2B) meetings and networking 
            with both other business stakeholders or community members, but also aviation regulators and air service 
            negotiators participating in the event. ​
        </p>
        <p>States mainly benefit from ICAN through enhanced efficiency and reduced costs by allowing:</p>
        <ul>
            <li>A one-stop approach to facilitate ASA negotiations/consultations.</li>
            <li>Saving time and money by avoiding separate individual trips to conduct talks.</li>
            <li>Enhance interaction between stakeholders.</li>
            <li>Facilitate dialogue among participants.</li>
            <li>Promote cooperation for the benefits of all participating states.</li>
            <li>Facilitation of Business to Business meetings and a facilitator of economic business for aviation 
                stakeholders – air operators, aircraft manufacturers, CAAs, Airport operators, Ground handlers 
                amongst others.</li>
        </ul>

